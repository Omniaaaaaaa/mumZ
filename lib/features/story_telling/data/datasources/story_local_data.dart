عَاشَ تُوبِي مَعَ إِيمِيلِي وَسَارَة لِسَنَوَاتٍ عَدِيدَةٍ، وَكَانَ لَدَيْهِ المَزِيدُ مِنَ المُغَامَرَاتِ.
ذَهَبُوا فِي رِحْلَاتٍ كَثِيرَةٍ، فَذَهَبُوا إِلَى الشَّاطِئِ، وَحَتَّى إِلَى حَدِيقَةِ الحَيَوَانَاتِ، وَالكَثِيرَ مِنَ الرِّحْلَاتِ المُخْتَلِفَةِ.
كَانَ يُحِبُّ تُوبِي اسْتِكْشَافَ العَالَمِ مَعَ أَصْدِقَائِهِ، وَكَانَ دَائِمًا يَتَعَلَّمُ أَشْيَاءً جَدِيدَةً.

فِي يَوْمٍ مِنَ الأَيَّامِ، كَانَ تُوبِي يَسْتَكْشِفُ الغَابَةَ، عِنْدَمَا صَادَفَ مَجْمُوعَةً مِنَ السَّلَاحِفِ، وَكَانَت السَّلَاحِفُ كُلُّهَا كِبَارًا فِي السِّنِّ، وَكَانُوا يُحْكُونَ قِصَصًا عَنْ الزَّمَنِ القَدِيمِ.

اِسْتَمَعَ تُوبِي إِلَى القِصَصِ، وَتَعَلَّمَ الكَثِيرَ عَن تَارِيخِ السَّلَاحِفِ.

وَعِنْدَمَا اِنْتَهَتِ السَّلَاحِفُ مِنْ سَرْدِ قِصَصِهَا، قَالَ تُوبِي:

“أَنَا سَعِيدٌ جِدًّا لأَنِّي قَابَلْتُكُمْ، فَأَنَا لَمْ أَلْتَقِ بِأَيِّ سَلْحُفَاةٍ أُخْرَى مِنْ قَبْلُ.”

ابْتَسَمَتِ السَّلَاحِفُ وَقَالَت:

“نَحْنُ سُعَدَاءُ لِأَنَّنَا قَابَلْنَاكَ أَيْضًا يَا تُوبِي، أَنْتَ سُلْحُفَاةٌ مُمَيَّزَةٌ جِدًّا.”

ابْتَسَمَ تُوبِي بِسَبَبِ مَا قَالَتْهُ لَهُ السَّلَاحِفُ، وَعَلِمَ أَنَّهُ سُلْحُفَاةٌ مُمَيَّزَةٌ، وَعَاشَ تُوبِي سَعِيدًا مَعَ إِيمِيلِي وَسَارَة، وَعَلِمَ أَنَّهُ سَيَكُونُ لَدَيْهِ دَائِمًا أَصْدِقَاءٌ، وَسَيَكُونُ لَدَيْهِ دَائِمًا مُغَامَرَاتٌ.
